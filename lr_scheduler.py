def generate_lr_scheduler(option, optimizer):
    ''' lr scheduler setting '''
    if option.lr_scheduler == 'exponential':
        lr_scheduler = torch.optim.lr_scheduler.ExponentialLR(optimizer, gamma=0.8)
    elif option.lr_scheduler == 'step':
        lr_scheduler = torch.optim.lr_scheduler.StepLR(optimizer=optimizer,
                                                        step_size=15,
                                                        gamma=0.5)
    elif option.lr_scheduler == 'plateau':
            lr_scheduler = torch.optim.lr_scheduler.ReduceLROnPlateau(optimizer=optimizer,
                                                                        mode='min',
                                                                        factor=0.5,
                                                                        patience=3,
                                                                        threshold=0.001,
                                                                        cooldown=3)
    elif option.lr_scheduler == 'cosine':
        lr_scheduler = CosineAnnealingWarmupRestarts(optimizer, first_cycle_steps=option.cosine_iter, 
            cycle_mult=2, max_lr=option.lr_init, min_lr=option.lr_min, warmup_steps=2, gamma=1)
    else:
        print("Optimizer name error")
        raise NameError
    print('LR_scheduler: ', lr_scheduler.__class__.__name__)
    return lr_scheduler
